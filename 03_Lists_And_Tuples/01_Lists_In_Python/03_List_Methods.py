#Methods in list
list = [2,1,3]

#Append
list.append(4)
print(list)

#Sort
list.sort()
print(list)

#Reverse sort
list.sort(reverse = True)
print(list)

#Reverse
list.reverse()
print(list)

#Insert element at index
list.insert(4,5)
print(list)

#Remove element
list.remove(5)
print(list)

#Remove element at index
list.pop(3)
print(list)
